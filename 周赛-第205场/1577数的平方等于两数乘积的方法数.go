func numTriplets(nums1 []int, nums2 []int) int {
	var f func(n1, n2 []int) int

	f = func(n1, n2 []int) int {
		var ans int
		if len(n2) < 2 {
			return 0
		}

		m := map[int]bool{}

		for j := 0; j < len(n2); j++ {
			for k := j + 1; k < len(n2); k++ {
				m[n2[j]*n2[k]] = true
			}
		}

		for i := 0; i < len(n1); i++ {
			tmp := n1[i] * n1[i]
			if _, has := m[tmp]; has {
				for j := 0; j < len(n2); j++ {
					for k := j + 1; k < len(n2); k++ {
						if n2[j]*n2[k] == tmp {
							ans++
						}
					}
				}
			}
		}

		return ans
	}

	return f(nums1, nums2) + f(nums2, nums1)
}

